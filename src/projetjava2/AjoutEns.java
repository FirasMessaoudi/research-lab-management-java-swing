    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String name=nom.getText().toString();
            String pren=prenom.getText().toString();
            String Age=age.getText().toString();
            String numtel=tel.getText().toString();
            String adremail=email.getText().toString();
            String CIN=cin.getText().toString();
            String nature=jComboBox1.getSelectedItem().toString();
            String Grade=jComboBox2.getSelectedItem().toString();
            String inst=institut.getText().toString();
            String univ=universite.getText().toString();
            String date=((JTextField)datedip.getDateEditor().getUiComponent()).getText();
            String photo=jTextField1.getText();
            
              boolean ok=true;
                    if(name.equals("") || HasAlphaNumeric(name)){
                    JOptionPane.showMessageDialog(this, " nom invalide");
                    ok=false;}
                    if(pren.equals("") || HasAlphaNumeric(pren)){
                         JOptionPane.showMessageDialog(this, "prenom invalide");
                           ok=false;}
                    if(Age.length()!=2 ||!numerique(Age) || !valideAge(Age)){
                             JOptionPane.showMessageDialog(this, "Age invalide");
                               ok=false;}
                    if(numtel.equals("")||!numerique(numtel)){
                         JOptionPane.showMessageDialog(this, "Numero du telephone invalide");
                           ok=false;}
                    if(!isValidEmailId(adremail)){
                    JOptionPane.showMessageDialog(this, "invaild email");
                      ok=false;}
                    if(!numerique(CIN)||CIN.length()!=8 ||cin.equals("00000000")){
                     JOptionPane.showMessageDialog(this, "invalid cin ");
                       ok=false;}
                    if(jComboBox1.getSelectedIndex()==0){
                         JOptionPane.showMessageDialog(this, "selectionner le nature du diplome");
                           ok=false;}
                    if(jComboBox2.getSelectedIndex()==0){
                        JOptionPane.showMessageDialog(this, "selectionner le grade");
                          ok=false;}
                    if(date.equals("")){
                    JOptionPane.showMessageDialog(this, "Vous devez saisir la date du dernier diplome");
                      ok=false;}
                    if(inst.equals("")||HasAlphaNumeric(inst)){
                        JOptionPane.showMessageDialog(this, "Vous devez saisir l'institut");
                          ok=false;}
                    if(univ.equals("")||HasAlphaNumeric(univ)){
                        JOptionPane.showMessageDialog(this, "Vous devez saisir l'institut");
                          ok=false;}
                   
           
            if(ok){
                PreparedStatement pst=con.prepareStatement("insert into chercheur values(?,?,?,?,?,?,?)");
                File f=new File(photo);
                FileInputStream inputStream= new FileInputStream(f);
                pst.setString(1, CIN);
                pst.setString(2, name);
                pst.setString(3, pren);
                pst.setString(4, numtel);
                pst.setString(5, adremail);
                pst.setString(6, Age);
                pst.setBlob(7, inputStream);
                pst.execute();
                PreparedStatement pst2=con.prepareStatement("insert into enseignant values(?,?,?,?,?,?,?)");
                pst2.setString(1, CIN);
                pst2.setString(2, date);
                pst2.setString(3, nature);
                pst2.setString(4, Grade);
                pst2.setString(5, inst);
                pst2.setString(6, univ);
                pst2.setString(7, "0");

                pst2.execute();
                JOptionPane.showMessageDialog(this, "Record inserted successfully");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String numtel=tel.getText();
        String adremail=email.getText();
        String CIN=cin.getText();
        String nature=jComboBox1.getSelectedItem().toString();
        String Grade=jComboBox2.getSelectedItem().toString();
        String inst=institut.getText();
        String univ=universite.getText();
        String date=((JTextField)datedip.getDateEditor().getUiComponent()).getText();
        String photo=jTextField1.getText();
        try{
            PreparedStatement st = con.prepareStatement("select * from chercheur where cin=? ");
            st.setString(1, CIN);
            rs=st.executeQuery();
            if(!rs.next()){
                JOptionPane.showMessageDialog(this, "L'enseignant portant la cin numero "+CIN+" n'existe pas dans la base");
            }
            else{
                if(!numtel.equals("")){
                    if(!numerique(numtel) || numtel.length()!=8){
                        JOptionPane.showMessageDialog(this, "Num tel incorrecte");
                    }
                    else{
                        PreparedStatement statement = con.prepareStatement("update chercheur set tel=? where cin=?" );
                        statement.setString(1,numtel);
                        statement.setString(2, CIN);
                        statement.execute();
                    }
                }
                if(!adremail.equals("")){
                    if(!isValidEmailId(adremail))
                    JOptionPane.showMessageDialog(this, "Email incorrecte");
                    else{
                        PreparedStatement statement = con.prepareStatement("update chercheur set email=? where cin=? ");
                        statement.setString(1,adremail);
                        statement.setString(2, CIN);
                        statement.execute();
                    }
                }
                if(jComboBox1.getSelectedIndex()>0){
                    if(HasAlphaNumeric(nature)){
                        JOptionPane.showMessageDialog(this, "Nom encadreur incorrecte");
                    }
                    else{
                        PreparedStatement statement = con.prepareStatement("update enseignant set natureDerDiplome=? where cin=? ");
                        statement.setString(1,nature);
                        statement.setString(2, CIN);
                        statement.execute();
                    }
                }
                if(jComboBox2.getSelectedIndex()>0){
                    if(HasAlphaNumeric(Grade)){
                        JOptionPane.showMessageDialog(this, "Nom encadreur incorrecte");
                    }
                    else{
                        PreparedStatement statement = con.prepareStatement("update enseignant set grade=? where cin=? ");
                        statement.setString(1,Grade);
                        statement.setString(2, CIN);
                        statement.execute();
                    }
                }
                if(!inst.equals("")){
                    if(HasAlphaNumeric(inst)){
                        JOptionPane.showMessageDialog(this, "Nom encadreur incorrecte");
                    }
                    else{
                        PreparedStatement statement = con.prepareStatement("update enseignant set institut=? where cin=? ");
                        statement.setString(1,inst);
                        statement.setString(2, CIN);
                        statement.execute();
                    }
                }
                if(!univ.equals("")){
                    if(HasAlphaNumeric(univ)){
                        JOptionPane.showMessageDialog(this, "Nom encadreur incorrecte");
                    }
                    else{
                        PreparedStatement statement = con.prepareStatement("update enseignant set universite=? where cin=? ");
                        statement.setString(1,univ);
                        statement.setString(2, CIN);
                        statement.execute();
                    }
                }
                if(!date.equals("")){
                    PreparedStatement statement = con.prepareStatement("update enseignant set dateDerDiplome=? where cin=? ");
                    statement.setString(1,date);
                    statement.setString(2, CIN);
                    statement.execute();
                }
                if(!photo.equals("")){
                    File f=new File(photo);
                    FileInputStream inputStream= new FileInputStream(f);
                    PreparedStatement statement = con.prepareStatement("update chercheur set photo=? where cin=?");
                    statement.setBlob(1, inputStream);
                    statement.setString(2, CIN);
                    statement.execute();
                }

                JOptionPane.showMessageDialog(this, "record updated successfully");
            }

        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

