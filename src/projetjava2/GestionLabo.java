    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String code=jTextField1.getText();
            String acro=jTextField2.getText();
            String logo=jTextField3.getText();
            String nom=jTextField4.getText();
            String adr=jTextField5.getText();
            String univ=jTextField6.getText();
            String domaine=jTextField7.getText();
            if(!code.equals("")){
                PreparedStatement statement = con.prepareStatement("update labo set code=? ");
               statement.setString(1, code);
               statement.execute();
            }
           
            if(!acro.equals("")){
                PreparedStatement statement = con.prepareStatement("update labo set acronyme=? ");
               statement.setString(1, acro);
               statement.execute();
            }
            if(!logo.equals("")){
               File f=new File(logo);
               FileInputStream inputStream= new FileInputStream(f);
               PreparedStatement statement = con.prepareStatement("update labo set logo=? ");
               statement.setBlob(1, inputStream);
               statement.execute();
            }
            if(!nom.equals("")){
                 PreparedStatement statement = con.prepareStatement("update labo set nom=? ");
               statement.setString(1, nom);
               statement.execute();
            }
            if(!adr.equals("")){
                 PreparedStatement statement = con.prepareStatement("update labo set adresse=? ");
               statement.setString(1, adr);
               statement.execute();
            }
            if(!univ.equals("")){
                PreparedStatement statement = con.prepareStatement("update labo set universite=? ");
               statement.setString(1, univ);
               statement.execute();
            }
            if(!domaine.equals("")){
                 PreparedStatement statement = con.prepareStatement("update labo set domaine=? ");
               statement.setString(1, domaine);
               statement.execute();
            }
            
            JOptionPane.showMessageDialog(GestionLabo.this, "Row updated successfully !!");
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

